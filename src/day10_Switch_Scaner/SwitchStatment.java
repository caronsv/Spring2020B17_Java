package day10_Switch_Scaner;

public class SwitchStatment {
    public static void main(String[] args) {

        switch (2){
            case 1:
            System.out.println("One");
            break;
            case 2:
                System.out.println("Two");
                break;
            default:
                System.out.println("Invalid case");
                break;
        }
    }
}
